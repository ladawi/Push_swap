#include "../includes/Push_swap.h"

int			check(int ac, char **av)
{
	int		i;
	int		x;

	i = 0;
	x = 0;
	while (++i < ac)
	{
		while (av[i][x])
		{
			if (!(ft_isdigit(av[i][x])))
				return (-1);
			x++;
		}
		x = 0;
	}
	return (0);
}
